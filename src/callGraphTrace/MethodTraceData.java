package callGraphTrace;

public class MethodTraceData {
	
	public static long trace;
	
	public static void addTrace(long methodId)
	{
		if(trace == 0)
			trace = methodId;
		
		else
		{
			trace = (trace << 20 ) & 0xfffffff;
		}
	}
}
